#include "push_swap.h"

void	fn_rrr(t_stack **a, t_stack **b, int j)
{
	if (!*a || !((*a)->next) || !*b || !((*b)->next))
		return ;
	fn_rra(a, 1);
	fn_rrb(b, 1);
	if (j == 0)
		write(1, "rrr\n", 4);
}
